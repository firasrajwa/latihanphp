<?php
$angka = 10;
$pembagi = 3;

// Operator / untuk mendapatkan hasil bagi
$hasil_bagi = $angka / $pembagi;

// Operator % untuk mendapatkan sisa bagi
$sisa_bagi = $angka % $pembagi;

echo "Hasil bagi 10 / 3 adalah: " . $hasil_bagi; // Output: 3.3333333333333
echo "<br>";
echo "Sisa bagi 10 % 3 adalah: " . $sisa_bagi;   // Output: 1
?>